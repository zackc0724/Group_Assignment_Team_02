    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
          if (facultyProfile != null) {
            facultyProfile.getPerson().setName(txtName.getText());
            facultyProfile.getPerson().setEmail(txtEmail.getText());
            facultyProfile.getPerson().setPhone(txtContact.getText());
            facultyProfile.getPerson().setPassword(new String(pwdPassword.getPassword()));

            JOptionPane.showMessageDialog(this, "Profile updated successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "Faculty profile not found!", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

