   private void populateProfileFields() {
    if (facultyProfile != null && facultyProfile.getPerson() != null) {
        txtName.setText(facultyProfile.getPerson().getName());
        txtEmail.setText(facultyProfile.getPerson().getEmail());
        txtContact.setText(facultyProfile.getPerson().getPhone());
        txtDepartment.setText(facultyProfile.getDepartment().getName()); // read-only
        pwdPassword.setText(facultyProfile.getPerson().getPassword());
    }

    }
