   private void populateProfileFields() {
        if (faculty != null) {
            txtName.setText(faculty.getName());
            txtEmail.setText(faculty.getEmail());
            txtContact.setText(faculty.getPhone());
            txtDepartment.setText(faculty.getDepartment()); // read-only
            
            // Find the password from AuthManager
            UserAccount ua = authManager.findAccountByLinkedId(faculty.getUniversityId());
            if (ua != null) {
                pwdPassword.setText(ua.getPassword());
            } else {
                pwdPassword.setText("****"); // Should not happen
            }
        }
    }
