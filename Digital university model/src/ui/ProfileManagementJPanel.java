    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
          if (faculty != null) {
            // Update the Person model
            faculty.setName(txtName.getText());
            faculty.setEmail(txtEmail.getText());
            faculty.setPhone(txtContact.getText());
            // Department is not editable
            
            String newPassword = new String(pwdPassword.getPassword());
            
            // Also update the password in the AuthManager
            UserAccount ua = authManager.findAccountByLinkedId(faculty.getUniversityId());
            if (ua != null && newPassword != null && !newPassword.isEmpty()) {
                ua.setPassword(newPassword);
            }

            JOptionPane.showMessageDialog(this, "Profile updated successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "Faculty profile not found!", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

