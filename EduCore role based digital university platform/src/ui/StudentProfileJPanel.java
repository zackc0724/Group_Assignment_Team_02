    private void populateProfileFields() {
        if (loggedInStudent != null) {
            txtName.setText(loggedInStudent.getName());
            txtEmail.setText(loggedInStudent.getEmail());
            txtContact.setText(loggedInStudent.getPhone());
            txtDepartment.setText(loggedInStudent.getDepartment()); // read-only

            // Find the password from AuthManager
            UserAccount ua = authManager.findAccountByLinkedId(loggedInStudent.getUniversityId());
            if (ua != null) {
                pwdPassword.setText(ua.getPassword());
            } else {
                pwdPassword.setText("****"); // Should not happen
            }
        }
    }

