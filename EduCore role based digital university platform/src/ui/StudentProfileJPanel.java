    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (loggedInStudent != null) {
            // Basic validation
             String name = txtName.getText();
             String email = txtEmail.getText();
             String phone = txtContact.getText();
             String newPassword = new String(pwdPassword.getPassword());

             if (name.trim().isEmpty() || email.trim().isEmpty()) {
                 JOptionPane.showMessageDialog(this, "Name and Email cannot be empty.", "Validation Error", JOptionPane.ERROR_MESSAGE);
                 return;
             }
             // Add more validation if needed (e.g., email format)


            // Update the Student model
            loggedInStudent.setName(name);
            loggedInStudent.setEmail(email);
            loggedInStudent.setPhone(phone);
            // Department is not editable

            // Also update the password in the AuthManager
            UserAccount ua = authManager.findAccountByLinkedId(loggedInStudent.getUniversityId());
            if (ua != null && newPassword != null && !newPassword.isEmpty()) {
                ua.setPassword(newPassword);
                 JOptionPane.showMessageDialog(this, "Profile updated successfully!");
            } else if (ua == null){
                 JOptionPane.showMessageDialog(this, "Could not find user account to update password.", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                 // Password field was empty, only update other fields
                 JOptionPane.showMessageDialog(this, "Profile (excluding password) updated successfully!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Student profile not found!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

